    async function servotest() {
        let statusConfig = {
            alpha: 0,
            starttime: 0,
            P: 0,
            control: false,
            mass: 0,
            param: 0,
            s1min: 0,
            s2min: 0,
            s3min: 0,
            s1max: 0,
            s2max: 0,
            s3max: 0,
            init: 0,
