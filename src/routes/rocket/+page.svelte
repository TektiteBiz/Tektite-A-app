        await invoke("servo_test", {
            config: statusConfig,
            max: servovar.endsWith("max"),
        });
        (status.config as any)[servovar] = servoval;
    }
    function updateServoVal() {
        servoval = (status.config as any)[servovar];
    }

    let playingFlight = false;
    async function playFlight() {
        let data = chartData!
            .filter((x) => x.state == 3 && x.vz > 0)
            .map((x) => {
                return {
                    delay: Number(x.time),
                    servo: Number(x.servo),
                };
            });

        // Make the delays all be relative to the previous delay
        for (let i = data.length - 1; i > 0; i--) {
            data[i].delay -= data[i - 1].delay;
        }
        data[0].delay = 0;
        data.push({
            delay: -1,
            servo: 0,
        });

        playingFlight = true;
        await invoke("play_flight", { data });
        playingFlight = false;
    }

    async function importFlightData() {
        let file = (await open({
            filters: [
                {
                    name: "Flight Data",
                    extensions: ["csv"],
                },
            ],
            multiple: false,
        })) as string;
        if (!file) {
            return;
        }
        let val = await readTextFile(file);
        let name = file.split("/").pop()!.split(".")[0];
        await writeTextFile(
            await join(await appDataDir(), $rocketName, name + ".csv"),
            val,
        );
        loadConfig();
    }

    function closeChart() {
        chartData = undefined;
        (CrosshairPlugin as any).resetZoom(chart); // Avoid "does not exist" error
        chart.destroy();
    }
</script>

