        calcSim();
    }

    let loadingSim = false;
    let simStartTime: number = 0;

    let showMotion = true;
    let showEnvironment = true;
    let showSim = true;
    function updateChartData() {
        let dat = structuredClone(data);
        if (!showMotion) {
            dat.datasets = dat.datasets.filter(
                (x) =>
                    x.label != "Altitude (m)" &&
                    x.label != "Vertical Velocity (m/s)" &&
                    x.label != "Horizontal Velocity (m/s)" &&
                    x.label != "Vertical Acceleration (m/s^2)",
            );
        }
        if (!showEnvironment) {
            dat.datasets = dat.datasets.filter(
                (x) =>
                    x.label != "Temperature (C)" &&
                    x.label != "Canard Angle (degrees)" &&
                    x.label != "Predicted Altitude (m)",
            );
        }
        if (!showSim) {
            dat.datasets = dat.datasets.filter(
                (x) =>
                    x.label != "Simulated Altitude (m)" &&
                    x.label != "Simulated Vertical Velocity (m/s)" &&
                    x.label != "Simulated Horizontal Velocity (m/s)" &&
                    x.label != "Simulated Vertical Acceleration (m/s^2)" &&
                    x.label != "Simulated Canard Angle (degrees)" &&
                    x.label != "Simulated Predicted Altitude (m)",
            );
        }

        chart.data = dat;
        chart.update();
    }

    function setChartData(name: string, time: number[], values: number[]) {
        let ind = data.datasets.findIndex((x) => x.label == name);
        data.datasets[ind].data = time.map((x, i) => ({
            x: x,
            y: values[i],
        }));
    }
    async function calcSim() {
        loadingSim = true;
        // Get index of nearest data time to simStartTime
        let idx = chartData!.findIndex((x) => x.time >= simStartTime * 1000);
        let res = (await invoke("calc_sim", {
            config,
            times: chartData!.map((x) => Number(x.time / 1000)).slice(idx),
            samples: chartData!.map((x) => Math.floor(x.samples)).slice(idx),
            vx0: Math.sqrt(chartData![idx].vx ** 2 + chartData![idx].vy ** 2),
            vz0: Number(chartData![idx].vz),
            x0: Number(chartData![idx].alt),
            temp: config.temp,
        })) as SimData;
        res.time = res.time.map((x) => Math.round(x * 1000) / 1000); // Fix floating point errors
        setChartData("Simulated Altitude (m)", res.time, res.alt);
        setChartData("Simulated Vertical Velocity (m/s)", res.time, res.vz);
        setChartData("Simulated Horizontal Velocity (m/s)", res.time, res.vx);
        setChartData(
            "Simulated Vertical Acceleration (m/s^2)",
            res.time,
            res.az,
        );
        setChartData("Simulated Canard Angle (degrees)", res.time, res.angle);
        setChartData(
            "Simulated Predicted Altitude (m)",
            res.time,
            res.predictedAlt,
        );
        updateChartData();
        loadingSim = false;
    }

