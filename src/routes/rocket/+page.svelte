    import {
        invalidUrl,
        object_equals,
        type Config,
        type Status,
        type SimData,
        rocketName,
    } from "$lib";
    import { listen } from "@tauri-apps/api/event";
    import Chart, { type ChartData } from "chart.js/auto";
