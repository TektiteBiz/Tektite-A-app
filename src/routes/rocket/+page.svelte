    import CrosshairPlugin from "chartjs-plugin-crosshair";
    let connected = false;

    let config: Config;
    let flightDataList: string[] = [];
    let savedStatus: Status;
    let status: Status;

    onMount(loadConfig);
    onMount(async () => {
        let conn = await invoke("is_connected");
        if (conn) {
            await loadStatus();
            connected = true;
        } else {
            connected = false;
        }
    });

    let rho: number = 0;
    $: config, (rho = calculateRho());

    function calculateRho() {
        if (!config) {
            return 0;
        }
        let p1 =
            6.1078 * Math.pow(10, (7.5 * config.temp) / (config.temp + 237.3));
        let pv = config.humidity * p1;
        let pd = config.pressure - pv;
        return (
            pd / (287.05 * (config.temp + 273.15)) +
            pv / (461.495 * (config.temp + 273.15))
        );
    }

    function calculate() {
        status.config.alpha = config.A * calculateRho();
        status.config.mass = config.mass - config.propellantMass / 1000;
        status.config.control = config.control;
        status.config.param = config.param;
        status.config.P = config.P;
        status.config.starttime = Math.round(config.startTime * 1000);
    }

    async function saveRocketConfig() {
        await invoke("config_write", { config: status.config });
        savedStatus = structuredClone(status);
        await loadStatus();
    }

    async function loadConfig() {
        const dataDir = await appDataDir();
        let val = await readTextFile(
            await join(dataDir, $rocketName, "conf.json"),
        );
        config = JSON.parse(val);

        flightDataList = (await invoke("read_flight_data", {
            path: await join(dataDir, $rocketName),
        })) as string[];
    }

    async function saveConfig() {
        await writeTextFile(
            await join(await appDataDir(), $rocketName, "conf.json"),
            JSON.stringify(config),
        );
        if (chartData) {
            calcSim();
        }
    }

    async function connect() {
        let succ = await invoke("connect");
        if (!succ) {
            message("Failed to connect. Is it plugged in?", {
                type: "warning",
            });
            connected = false;
        } else {
            await loadStatus();
            message("Successfully connected!", { type: "info" });
            connected = true;
        }
    }

    async function loadStatus() {
        status = await invoke("get_status");
        savedStatus = structuredClone(status);
        updateServoVal();
    }

    async function disconnect() {
        await invoke("disconnect");
        connected = false;
    }

    async function changeThrustCurve() {
        let file = (await open({
            filters: [
                {
                    name: "Thrust Curve",
                    extensions: ["csv"],
                },
            ],
        })) as string | null;
        if (!file) {
            return;
        }
        let val = await readTextFile(file);
        let res = Papa.parse(val).data as string[][];
        config.thrustCurveName = res[0][1];
        config.thrustCurveTime = [];
        config.thrustCurveForce = [];
        for (let i = 5; i < res.length; i++) {
            if (
                i == 5 ||
                Number(res[i][0]) >
                    config.thrustCurveTime[config.thrustCurveTime.length - 1]
            ) {
                config.thrustCurveTime.push(Number(res[i][0]));
                config.thrustCurveForce.push(Number(res[i][1]));
            }
        }
        await saveConfig();
    }

    let flightDataName = "";
    let loadingData = false;
    let dataProgress = 0;
    async function readFlightData() {
        if (invalidUrl(flightDataName)) {
            message("Invalid flight data name", { type: "warning" });
            return;
        }
        let path = await join(
            await appDataDir(),
            $rocketName,
            flightDataName + ".csv",
        );
        dataProgress = 0;
        loadingData = true;

        await invoke("read_data", { path });

        loadingData = false;
        flightDataName = "";

        status.has_data = false;

        await loadConfig();
    }

    onMount(() => {
        listen("recvdata", (e) => {
            dataProgress = e.payload as number;
        });
        listen("disconnect", (e) => {
            connected = false;
        });
    });

    async function deleteFlightData(name: string) {
        if (
            !(await confirm(`Delete flight data ${name}?`, { type: "warning" }))
        ) {
            return;
        }
        await removeFile(
            await join(await appDataDir(), $rocketName, name + ".csv"),
        );
        await loadConfig();
    }

    let chartData: Record<string, number>[] | undefined;
    let chartDataName = "";
    let chart: Chart;
    let data: ChartData;
